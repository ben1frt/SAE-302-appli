// class permettant de créer un objet JSON avec une chaine de caractères

public class JSONObject {

    // constructeur de la classe
    public JSONObject (String data) {
    }

    // affiche les données d'un fichier JSON
    public void showData (String data) {
        // récupère les données de la requete
        System.out.println(data);
    }


    // méthode permettant de récupérer les données d'un fichier JSON
    public String getString (String data) {
        // récupère les données de la requete
        return data;
    }
    
}